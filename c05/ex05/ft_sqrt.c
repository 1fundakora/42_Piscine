int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (nb == 1)
		return (1);
	while (a <= nb / 2 && a <= 46340)
	{
		if (a * a == nb)
			return (a);
		a++;
	}
	return (0);
}
